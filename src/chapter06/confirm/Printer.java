package chapter06.confirm;

public class Printer {

	void println(int i) {
		System.out.println(i);
	}

	void println(boolean b) {
		System.out.println(b);
	}

	void println(double d) {
		System.out.println(d);
	}

	void println(String string) {
		System.out.println(string);
	}
	
}
